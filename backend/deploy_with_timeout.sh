        echo -e "${GREEN}✅ Local Docker build successful${NC}"
        docker rmi bunklogs-backend-test 2>/dev/null || true
    else
        echo -e "${RED}❌ Local Docker build failed. Please fix issues before deploying.${NC}"
        exit 1
    fi
fi

# Start deployment
