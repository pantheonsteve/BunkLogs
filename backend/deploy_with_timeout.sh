    echo -e "${YELLOW}📝 Initializing EB configuration...${NC}"
    eb init --platform docker --region us-east-1
fi

# Create deployment with timeout monitoring
echo -e "${BLUE}📦 Deploying to Elastic Beanstalk...${NC}"

