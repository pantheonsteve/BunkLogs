    sleep 30
    show_elapsed_time
    
    if check_timeout_warning; then
        echo -e "${YELLOW}💡 Tip: Multi-stage Docker build should reduce build time significantly${NC}"
    fi
done

# Wait for deployment to complete
wait $DEPLOY_PID
