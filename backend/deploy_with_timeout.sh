# Function to display elapsed time
show_elapsed_time() {
    local current_time=$(date +%s)
    local elapsed=$((current_time - BUILD_START_TIME))
    local minutes=$((elapsed / 60))
    local seconds=$((elapsed % 60))
    echo -e "${BLUE}⏱️  Elapsed time: ${minutes}m ${seconds}s${NC}"
}

# Function to check if timeout is approaching
check_timeout_warning() {
    local current_time=$(date +%s)
    local elapsed=$((current_time - BUILD_START_TIME))
    local elapsed_minutes=$((elapsed / 60))
    local warning_threshold=$((TIMEOUT_MINUTES - 5))
    
    if [ $elapsed_minutes -ge $warning_threshold ]; then
        echo -e "${YELLOW}⚠️  WARNING: Approaching timeout threshold (${elapsed_minutes}/${TIMEOUT_MINUTES} minutes)${NC}"
        return 0
    fi
    return 1
}

# Pre-deployment checks
echo -e "${YELLOW}📋 Pre-deployment checks...${NC}"

# Activate virtual environment if it exists
if [ -d ".venv" ]; then
    echo -e "${GREEN}✅ Activating virtual environment...${NC}"
    source .venv/bin/activate
fi

# Check if EB CLI is installed
if ! command -v eb &> /dev/null; then
    echo -e "${RED}❌ EB CLI not found. Please install it first.${NC}"
    exit 1
fi

# Check if we're in the right directory
if [ ! -f "Dockerfile" ]; then
    echo -e "${RED}❌ Dockerfile not found. Please run from the backend directory.${NC}"
    exit 1
fi

# Check if .ebextensions timeout config exists
if [ ! -f ".ebextensions/00_timeout.config" ]; then
    echo -e "${RED}❌ Timeout configuration not found. Please ensure 00_timeout.config exists.${NC}"
    exit 1
fi

echo -e "${GREEN}✅ Pre-deployment checks passed${NC}"

# Display timeout configuration
